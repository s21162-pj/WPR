<?php
function dodaj($liczba1, $liczba2) {
    echo $liczba1 + $liczba2;
}

function odejmij($liczba1, $liczba2) {
    echo $liczba1 - $liczba2;
}

function pomnóż($liczba1, $liczba2) {
    echo $liczba1 * $liczba2;
}

function podziel($liczba1, $liczba2) {
    echo $liczba1 / $liczba2;
}